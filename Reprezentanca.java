public class Reprezentanca {
    private String okrajsava;
    private String drzava;

    public Reprezentanca(String okrajsava, String drzava) {
        this.okrajsava = okrajsava;
        this.drzava = drzava;
    }

    public String getOkrajsava() {
        return okrajsava;
    }

    public String getDrzava() {
        return drzava;
    }
}